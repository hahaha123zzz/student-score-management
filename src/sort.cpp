#include "sort.h"

namespace sort {

    // ========================================================================
    // 冒泡排序（Bubble Sort）
    // ========================================================================
    // 算法思想：像水里的气泡一样，大的元素逐渐"浮"到数组末尾。
    //
    // 具体步骤（升序排列）：
    //   - 从数组头部开始，依次比较相邻的两个元素 arr[j] 和 arr[j+1]
    //   - 如果 arr[j] > arr[j+1]，就交换它们（让大的往后走）
    //   - 每完成一轮外层循环，当前未排序部分的最大值就被推到了最后
    //   - 重复 n-1 轮后，整个数组有序
    //
    // 举例：数组 [5, 3, 8, 1]
    //   第1轮：比较5和3→交换 → [3,5,8,1] → 比较5和8→不动 → 比较8和1→交换 → [3,5,1,8]  （8到位）
    //   第2轮：[3,5,1,8] → 比较3和5→不动 → 比较5和1→交换 → [3,1,5,8]  （5到位）
    //   第3轮：[3,1,5,8] → 比较3和1→交换 → [1,3,5,8]  （完成）
    //
    // 时间复杂度：O(n^2)（最坏/平均），O(n)（最好：已有序）
    // 空间复杂度：O(1)
    // ========================================================================
    void bubbleSort(std::vector<double>& arr) {
        int n = (int)arr.size();                    // 获取数组长度
        for (int i = 0; i < n - 1; i++) {           // 外层循环：需要 n-1 轮
            for (int j = 0; j < n - 1 - i; j++) {   // 内层循环：每轮比较范围缩小（最后 i 个已排好）
                if (arr[j] > arr[j + 1]) {           // 如果前一个比后一个大，交换
                    double tmp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = tmp;
                }
            }
        }
    }

    // ========================================================================
    // 选择排序（Selection Sort）
    // ========================================================================
    // 算法思想：每次从"未排序部分"选出最小值，放到"已排序部分"的末尾。
    //
    // 具体步骤（升序排列）：
    //   - 第 i 轮（从 0 开始）：在区间 [i, n-1] 中找到最小元素的下标
    //   - 如果最小元素不在位置 i，就把 arr[i] 和 arr[minIdx] 交换
    //   - 交换后，位置 i 就是当前最小值，已排序部分向右扩展一位
    //   - 重复 n-1 轮后完成排序
    //
    // 举例：数组 [5, 3, 8, 1]
    //   第0轮：找 [0~3] 中最小值→1在下标3→交换 arr[0]和arr[3] → [1, 3, 8, 5]
    //   第1轮：找 [1~3] 中最小值→3在下标1（已在正确位置）→ [1, 3, 8, 5]
    //   第2轮：找 [2~3] 中最小值→5在下标3→交换 arr[2]和arr[3] → [1, 3, 5, 8]
    //
    // 时间复杂度：O(n^2)（无论数据如何，都要比较约 n^2/2 次）
    // 空间复杂度：O(1)
    // ========================================================================
    void selectionSort(std::vector<double>& arr) {
        int n = (int)arr.size();                     // 获取数组长度
        for (int i = 0; i < n - 1; i++) {            // 进行 n-1 轮选择
            int minIdx = i;                           // 假设当前位置是最小值
            for (int j = i + 1; j < n; j++) {         // 在未排序部分寻找真正的最小值
                if (arr[j] < arr[minIdx]) minIdx = j; // 发现更小的，更新最小下标
            }
            if (minIdx != i) {                        // 如果最小值不在当前位置，交换
                double tmp = arr[i];
                arr[i] = arr[minIdx];
                arr[minIdx] = tmp;
            }
        }
    }

    // ========================================================================
    // 快速排序（Quick Sort）—— 递归实现（升序）
    // ========================================================================
    // 算法思想（分治法 Divide and Conquer）：
    //   1. 选一个"基准值"（pivot，这里选第一个元素）
    //   2. 通过双指针 i（从左）、j（从右）将数组划分为左右两部分：
    //        左边所有元素 ≤ pivot，右边所有元素 ≥ pivot
    //   3. 基准值放到它最终的正确位置上
    //   4. 递归地对左半部分和右半部分进行同样操作
    //   5. 当子数组长度 ≤ 1 时不再继续
    //
    // 为什么快？每轮把问题规模减半，深挖而不是广撒。
    //
    // 举例：数组 [5, 3, 8, 1, 7]，pivot=5
    //   j先左移找到1（比5小），放到左边 → [1, 3, 8, 1, 7]
    //   i右移找到8（比5大），放到右边   → [1, 3, 8, 8, 7]
    //   i==j时停止，pivot归位           → [1, 3, 5, 8, 7]
    //   递归处理左边 [1,3] 和右边 [8,7]
    //
    // 时间复杂度：O(n log n)（平均），O(n^2)（最坏：每次pivot都是最值）
    // 空间复杂度：O(log n)（递归栈深度）
    // ========================================================================
    void quickSort(std::vector<double>& arr, int left, int right) {
        if (left >= right) return;                     // 递归终止：区间为空或只有一个元素
        double pivot = arr[left];                       // 选取最左边的元素作为基准值
        int i = left, j = right;                        // 双指针：i从左往右，j从右往左
        while (i < j) {                                 // 当 i 和 j 未相遇时继续
            while (i < j && arr[j] >= pivot) j--;       // j 向左移动，直到找到一个小于 pivot 的元素
            if (i < j) arr[i++] = arr[j];               // 把该元素放到左边（i位置），然后 i 右移
            while (i < j && arr[i] <= pivot) i++;       // i 向右移动，直到找到一个大于 pivot 的元素
            if (i < j) arr[j--] = arr[i];               // 把该元素放到右边（j位置），然后 j 左移
        }
        arr[i] = pivot;                                 // 基准值归位——此时 i 就是它的最终位置
        quickSort(arr, left, i - 1);                    // 递归处理左半部分
        quickSort(arr, i + 1, right);                   // 递归处理右半部分
    }

    // 快速排序的便捷入口——对整个数组排序
    void quickSort(std::vector<double>& arr) {
        if (arr.size() > 1)                             // 至少有两个元素才需要排序
            quickSort(arr, 0, (int)arr.size() - 1);     // 调用递归版本，区间为整个数组
    }

    // ========================================================================
    // 带索引的快速排序（降序）—— 同时排序成绩和对应的原始索引
    // ========================================================================
    // 原理：和普通快排一样，但每次交换/移动 arr 元素时，
    //       同步移动 indices 中对应的元素，保证两个数组始终对齐。
    //
    // 用途：例如要对成绩排名，但需要知道第1名、第2名分别是第几个学生。
    //       初始化 indices = {0, 1, 2, 3, ...}（原始学号/下标），
    //       排序后 indices 记录了原始位置的映射关系。
    //
    // 注意：此函数是降序排列（从大到小），与上面的升序快排相反！
    //       arr[j] <= pivotScore（找更大的放左边）
    // ========================================================================
    void quickSortWithIndex(std::vector<double>& scores, std::vector<int>& indices, int left, int right) {
        if (left >= right) return;                     // 递归终止条件
        double pivotScore = scores[left];               // 成绩的基准值
        int pivotIdx = indices[left];                   // 对应的索引基准值（需要和成绩同步归位）
        int i = left, j = right;                        // 双指针
        while (i < j) {                                 // 分区过程
            while (i < j && scores[j] <= pivotScore) j--; // j 向左找比基准值"大"的（降序：大值放左边）
            if (i < j) {
                scores[i] = scores[j];                   // 移动成绩
                indices[i++] = indices[j];               // 同步移动索引
            }
            while (i < j && scores[i] >= pivotScore) i++; // i 向右找比基准值"小"的（降序：小值放右边）
            if (i < j) {
                scores[j] = scores[i];                   // 移动成绩
                indices[j--] = indices[i];               // 同步移动索引
            }
        }
        scores[i] = pivotScore;                          // 成绩基准值归位
        indices[i] = pivotIdx;                           // 索引基准值归位——两者始终在同一位置
        quickSortWithIndex(scores, indices, left, i - 1);  // 递归左半
        quickSortWithIndex(scores, indices, i + 1, right); // 递归右半
    }

    // 带索引快速排序的便捷入口——对整个数组排序
    void quickSortWithIndex(std::vector<double>& scores, std::vector<int>& indices) {
        if (scores.size() > 1)                                                   // 至少两个元素才排序
            quickSortWithIndex(scores, indices, 0, (int)scores.size() - 1);      // 调用递归版本
    }

}
